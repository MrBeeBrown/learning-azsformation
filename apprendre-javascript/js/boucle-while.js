/**
 * Déroulement du programme
 * - On crée une variable compteur
 * - On l'initialise à 0
 * - Tant que compteur sera inférieur ou égale à 10
 * - Nous affichons sa valeur
 * - Nous incrémentons de la variable compteur  
 */

let compteur = 0;

while (compteur <= 10) {
    console.log(`Notre variable compteur vaut : ${compteur}`);
    compteur++;
}
